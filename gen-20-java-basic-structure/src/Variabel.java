public class Variabel {

    public static void main(String[] args){
        //Membuat variabel
        String nama, alamat; //Tipe Data String untuk menyimpan teks
        int usia; //Tipe Data Interger untuk menyimpan nilai numerik dengan bilangan bulat
        double tinggi; //Tipe Data Double untuk menyimpan nilai numerik yang memiliki koma

        //Mengisi variabel
        nama = "Arnanda";
        alamat = "Subang";
        usia = 23;
        tinggi = 171.2;

        //Mencetak isi variabel
        System.out.println("Nama: " + nama);
        System.out.println("Alamat: " + alamat);
        System.out.println("Usia: " + usia);
        System.out.println("Tinggi: " + tinggi);
    }
}